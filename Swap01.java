//swapping for three numbers with fourth variable//
public class Swap01{
public static void main(String args[]){

int a=8;
int b=7;
int c=9;

System.out.println("BEFORE SWAPPING FOR THREE NUMBERS");
System.out.println("a ="+a);
System.out.println("b ="+b);
System.out.println("c ="+c);
int d=a;  //d=8
a=b;      //a=b a=7
b=c;      //b=c b=9
c=d;      //c=d c=8
System.out.println("AFTER SWAPPING FOR THREE NUMBERS");
System.out.println("a="+a);
System.out.println("b="+b);
System.out.println("c="+c);


}
}  
